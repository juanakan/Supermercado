//inicializamos las frutas y damos valor false si no hay ninguna en el carro
let precioPera = 0;
let hayPera=false;

let precioNaranja = 0;
let hayNaranja=false;

let precioZanahoria = 0;
let hayZanahoria=false;

let precioSandia = 0;
let haySandia=false;

let precioPepino = 0;
let hayPepino=false;

let precioPimiento = 0;
let hayPimiento=false;

let precioCebolla = 0;
let hayCebolla=false;

let precioPlatano = 0;
